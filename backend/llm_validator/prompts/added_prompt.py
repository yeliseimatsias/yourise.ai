"""
Промпт для проверки добавленного текста.
"""

from .base_prompt import BasePrompt


class AddedPrompt(BasePrompt):
    """Промпт для проверки добавленного текста."""
    
    @classmethod
    def build(cls, change_data: dict, law_data: dict) -> str:
        """
        Строит промпт для проверки добавления.
        
        Args:
            change_data: Данные об изменении
            law_data: Данные о законе
            
        Returns:
            Полный текст промпта
        """
        change_type_str = cls.format_change_type('added')
        
        prompt = f"""{cls.SYSTEM_MESSAGE}

{cls.get_few_shot_examples()}

=== КОНТЕКСТ ПРОВЕРКИ ===
Тип изменения: {change_type_str}
Номер элемента: {change_data.get('element_number', 'не указан')}

ДОБАВЛЕННЫЙ ТЕКСТ:
{change_data['new_text']}

Контекст (где размещен новый текст):
{change_data['full_chunk']}

=== СТАТЬЯ ЗАКОНА ДЛЯ СРАВНЕНИЯ ===
Источник: {law_data['law_reference']}
Уровень иерархии: {law_data.get('hierarchy_level', 'не указан')}
Релевантность: {law_data.get('similarity', 0):.0%}

Текст статьи:
{law_data['chunk_text']}

=== ЗАДАНИЕ ===
Проанализируй, соответствует ли ДОБАВЛЕННЫЙ текст требованиям данной статьи закона.

КРИТЕРИИ ПРОВЕРКИ:
1. Не противоречит ли новый текст закону напрямую?
2. Не ограничивает ли он права, гарантированные законом?
3. Не создает ли он обязанностей, не предусмотренных законом?
4. Соответствует ли процедура, описанная в новом тексте, законной процедуре?
5. Не дублирует ли текст уже существующие нормы?

Особое внимание: добавление новых ограничений или обязанностей часто создает косвенные противоречия!

Ответь строго в формате JSON без markdown-разметки."""
        
        return prompt
