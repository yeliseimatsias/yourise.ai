"""
Промпт для проверки удаленного текста.
"""

from .base_prompt import BasePrompt


class DeletedPrompt(BasePrompt):
    """Промпт для проверки удаленного текста."""
    
    @classmethod
    def build(cls, change_data: dict, law_data: dict) -> str:
        """
        Строит промпт для проверки удаления.
        
        Args:
            change_data: Данные об изменении
            law_data: Данные о законе
            
        Returns:
            Полный текст промпта
        """
        change_type_str = cls.format_change_type('deleted')
        
        prompt = f"""{cls.SYSTEM_MESSAGE}

{cls.get_few_shot_examples()}

=== КОНТЕКСТ ПРОВЕРКИ ===
Тип изменения: {change_type_str}
Номер элемента: {change_data.get('element_number', 'не указан')}

УДАЛЕННЫЙ ТЕКСТ:
{change_data.get('old_text', '[не указано]')}

Контекст (откуда удален текст):
{change_data['full_chunk']}

=== СТАТЬЯ ЗАКОНА ДЛЯ СРАВНЕНИЯ ===
Источник: {law_data['law_reference']}
Уровень иерархии: {law_data.get('hierarchy_level', 'не указан')}
Релевантность: {law_data.get('similarity', 0):.0%}

Текст статьи:
{law_data['chunk_text']}

=== ЗАДАНИЕ ===
Проанализируй, является ли УДАЛЕНИЕ этого текста законным.

ВАЖНО: Мы проверяем не сам текст, а факт его УДАЛЕНИЯ из документа!

КРИТЕРИИ ПРОВЕРКИ:
1. Требует ли закон ОБЯЗАТЕЛЬНОГО наличия такой нормы в документе?
2. Не создает ли удаление пробел в правовом регулировании?
3. Не лишает ли удаление сторону каких-либо прав или гарантий?
4. Не противоречит ли удаление вышестоящим актам (если документ - локальный акт)?
5. Был ли удаленный текст необходим для реализации требований закона?

ТИПЫ НАРУШЕНИЙ ПРИ УДАЛЕНИИ:
- direct: Удалена ОБЯЗАТЕЛЬНАЯ норма, требуемая законом
- indirect: Удаление создает неопределенность или риски
- none: Удаление не влияет на соответствие закону (например, дублирующая норма)

Ответь строго в формате JSON без markdown-разметки."""
        
        return prompt